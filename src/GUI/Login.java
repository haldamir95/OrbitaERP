        //probando conexion
        db database = new db();
        boolean log = false;
        String consulta = "SELECT * "
                        + "FROM persona "
                        + "WHERE usr = '"+jTextField1.getText()+"' "
                        + "AND clave = '"+jPasswordField1.getText()+"' "
                        + "AND tipo_persona = "+((ComboItem)jComboBox1.getSelectedItem()).getValue();
